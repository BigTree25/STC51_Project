#include <REGX52.H>
#include "Delay.h"
#include "MatrixLED.h"

unsigned char Animation[]={
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0xFF,0x08,0x08,0x08,0xFF,0x00,0x0E,0x15,
	0x15,0x15,0x08,0x00,0xFE,0x01,0x02,0x00,
	0xFE,0x01,0x02,0x00,0x0E,0x11,0x11,0x0E,
	0x00,0xFD,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
};

void main()
{
	unsigned char i,j,count;
	MatrixLED_Init();
	while(1)
	{
		for(i=0;i<32-8;i++)
		{
			MatrixLED_ShowColumn(i+1,Animation[i+j]);	
		}
		count++;
		if(count>5)
		{
			count=0;
			j++;
			if(j>40)
			{
				j=0;
			}
		}
	}
}